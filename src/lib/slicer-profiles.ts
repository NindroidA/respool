// ─── Detection ──────────────────────────────────────────

export interface DetectionResult {
  profile: SlicerProfile;
  version: string | null;
  confidence: "high" | "medium" | "low";
}

/**
 * Auto-detect the slicer that generated a G-code file by scanning
 * the first ~150 lines for known header signatures.
 */
export function detectSlicer(text: string): DetectionResult {
  // Only scan the first 150 lines for efficiency
  const headerLines = text.split("\n", 150).join("\n");

  // Check each profile in priority order (most specific first)
  const profileOrder: SlicerId[] = [
    "orcaslicer",    // check before bambustudio (Orca is a Bambu fork)
    "bambustudio",
    "prusaslicer",
    "superslicer",   // check before prusaslicer patterns overlap
    "simplify3d",
    "cura",
    "ideamaker",
    "ankermaker",    // last — its pattern is less specific
  ];

  for (const id of profileOrder) {
    const profile = SLICER_PROFILES[id];
    if (profile.headerPattern.test(headerLines)) {
      let version: string | null = null;
      if (profile.versionExtractor) {
        const match = headerLines.match(profile.versionExtractor);
        if (match) version = match[1];
      }
      return { profile, version, confidence: "high" };
    }
  }

  // Heuristic fallback: check for layer marker style
  if (/^;LAYER_CHANGE/im.test(headerLines)) {
    // Prusa-family style but no header match
    return { profile: SLICER_PROFILES.prusaslicer, version: null, confidence: "medium" };
  }
  if (/^;LAYER:\d+/im.test(text.split("\n", 500).join("\n"))) {
    // Cura-family style
    return { profile: SLICER_PROFILES.cura, version: null, confidence: "medium" };
  }
  if (/^; layer \d+/im.test(text.split("\n", 500).join("\n"))) {
    return { profile: SLICER_PROFILES.simplify3d, version: null, confidence: "medium" };
  }

  return { profile: SLICER_PROFILES.generic, version: null, confidence: "low" };
}

// ─── Metadata Extraction ────────────────────────────────

export interface GcodeMetadata {
  slicer: DetectionResult;
  density: number | null;
  material: string | null;
  diameter: number | null;
  color: string | null;
  toolChangeCount: number;
  isMultiMaterial: boolean;
}

/**
 * Extract all metadata from G-code header without full parsing.
 * Used for quick validation and UI feedback before running the full parser.
 */
export function extractMetadata(text: string): GcodeMetadata {
  const slicer = detectSlicer(text);
  const profile = slicer.profile;
  const headerText = text.split("\n", 300).join("\n");

  let density: number | null = null;
  let material: string | null = null;
  let diameter: number | null = null;
  let color: string | null = null;

  if (profile.densityPattern) {
    const m = headerText.match(profile.densityPattern);
    if (m) density = parseFloat(m[1]);
  }
  if (profile.materialPattern) {
    const m = headerText.match(profile.materialPattern);
    if (m) material = m[1].trim();
  }
  if (profile.diameterPattern) {
    const m = headerText.match(profile.diameterPattern);
    if (m) diameter = parseFloat(m[1]);
  }
  if (profile.colorPattern) {
    const m = headerText.match(profile.colorPattern);
    if (m) color = `#${m[1]}`;
  }

  // Count tool changes to detect AMS/multi-material
  let toolChangeCount = 0;
  if (profile.toolChangePattern) {
    const lines = text.split("\n");
    const seen = new Set<string>();
    for (const line of lines) {
      const m = line.match(profile.toolChangePattern);
      if (m && !seen.has(m[1])) {
        seen.add(m[1]);
        toolChangeCount++;
      }
    }
  }

  return {
    slicer,
    density,
    material,
    diameter,
    color,
    toolChangeCount,
    isMultiMaterial: toolChangeCount > 1,
  };
}

// ─── Validation Helpers ─────────────────────────────────

export interface ValidationResult {
  valid: boolean;
  error?: string;
  warning?: string;
}

/**
 * Validate that a file is parseable G-code before attempting full parse.
 */
export function validateGcodeFile(
  content: string,
  fileName: string,
): ValidationResult {
  // Empty file
  if (!content || content.trim().length === 0) {
    return { valid: false, error: "File is empty." };
  }

  // Binary detection — check first 512 bytes for non-printable chars
  const sample = content.slice(0, 512);
  const nonPrintable = sample.split("").filter(
    (c) => {
      const code = c.charCodeAt(0);
      return code < 9 || (code > 13 && code < 32 && code !== 27);
    },
  ).length;
  if (nonPrintable > 5) {
    const ext = fileName.split(".").pop()?.toLowerCase();
    if (ext === "3mf" || ext === "zip") {
      return {
        valid: false,
        error: "This is a .3mf file, not G-code. Export as G-code (.gcode) from your slicer first.",
      };
    }
    if (ext === "stl" || ext === "obj") {
      return {
        valid: false,
        error: `This is a .${ext} model file, not G-code. Slice it first in your slicer software.`,
      };
    }
    return {
      valid: false,
      error: "This file contains binary data and doesn't appear to be G-code.",
    };
  }

  // Check for at least some G-code commands
  const hasGcode = /^[GMT]\d+/m.test(content);
  if (!hasGcode) {
    return {
      valid: false,
      error: "No G-code commands found in this file. Make sure you're uploading a sliced .gcode file.",
    };
  }

  // File size warning
  if (content.length > 50 * 1024 * 1024) {
    return {
      valid: true,
      warning: "Large file detected (>50MB). Parsing may take several seconds.",
    };
  }

  return { valid: true };
}

// ─── Unit Conversion ────────────────────────────────────

/**
 * Convert filament measurement to grams based on unit.
 * @param value - the numeric value from the G-code comment
 * @param unit - what unit the value is in
 * @param density - material density in g/cm³
 * @param diameter - filament diameter in mm (default 1.75)
 */
export function convertToGrams(
  value: number,
  unit: "grams" | "mm" | "meters" | "cm3",
  density: number,
  diameter: number = 1.75,
): number {
  const area = Math.PI * (diameter / 2) ** 2; // mm²

  switch (unit) {
    case "grams":
      return value;
    case "mm":
      // length_mm × area_mm² = volume_mm³, ÷1000 for cm³, × density
      return (value * area * density) / 1000;
    case "meters":
      // meters → mm, then same as above
      return (value * 1000 * area * density) / 1000;
    case "cm3":
      // volume_cm³ × density
      return value * density;
  }
}
