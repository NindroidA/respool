/**
 * Slicer profile definitions for multi-slicer G-code parsing.
 * Each profile defines how a specific slicer formats its G-code output,
 * enabling auto-detection and correct parsing of layer data.
 */

// ─── Types ──────────────────────────────────────────────

export type SlicerId =
  | "ankermaker"
  | "cura"
  | "prusaslicer"
  | "orcaslicer"
  | "bambustudio"
  | "simplify3d"
  | "superslicer"
  | "ideamaker"
  | "generic";

export interface WeightPattern {
  pattern: RegExp;
  unit: "grams" | "mm" | "meters" | "cm3";
}

export interface PauseInstruction {
  title: string;
  steps: string[];
  command: string; // M600, M0, or slicer-specific
  note?: string;
}

export interface SlicerProfile {
  id: SlicerId;
  name: string;
  // Detection — matched against first 150 lines
  headerPattern: RegExp;
  versionExtractor?: RegExp;

  // Parsing
  layerMarker: RegExp;
  weightPatterns: WeightPattern[];
  densityPattern?: RegExp;
  materialPattern?: RegExp;
  diameterPattern?: RegExp;
  colorPattern?: RegExp;

  // AMS / Multi-material
  isMultiMaterial: boolean;
  toolChangePattern?: RegExp;

  // UI — how to set up pauses in this slicer
  pauseInstructions: PauseInstruction;
}

